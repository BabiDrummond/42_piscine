#include <unistd.h>

void	aff_n(void)
{
	char	n;
	char	slash_n;

	n = 'n';
	slash_n = '\n';
	write(1, &n, 1);
	write(1, &slash_n, 1);
}

int	main(void)
{
	aff_n();
}
